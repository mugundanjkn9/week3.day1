package org.college;

public class College {
	
	public String collegeName(String name) {
		System.out.println(" College Name : "+name);
		return name;		
	}
	
	public long collegeCode(long code) {
		System.out.println(" College Code : "+code);
		return code;		
	}
	
	public int collegeRank(int rank) {
		System.out.println(" College Rank : "+rank);
		return rank;		
	}
	
}
